#include "../minishell.h"

void	echo_buildin(char **cmd)
{
	int	i;
	int	cut_newline;

	cut_newline = 0;
	if (ft_strcmp(cmd[1], "-n") == 0)
	{
		cut_newline = 1;
		cmd++;
	}
	i = 1;
	while (cmd[i] != NULL)
	{
		printf("%s", cmd[i]);
		if (cmd[i + 1] != NULL)
			printf(" ");
		i++;
	}
	if (!cut_newline)
		printf("\n");
}
